package com.acadgild.model;

public class Books {

	private String author;

	private String title;

	private String publicationtyear;

	private String publication;

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPublicationtyear() {
		return publicationtyear;
	}

	public void setPublicationtyear(String publicationtyear) {
		this.publicationtyear = publicationtyear;
	}

	public String getPublication() {
		return publication;
	}

	public void setPublication(String publication) {
		this.publication = publication;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	private String price;
}
